const x = 5;

// dodawanie, odejmowanie, mnożenie, dzielenie
console.log('5 + 2 =', x + 2);
console.log('5 - 1 =', x - 1);
console.log('5 * 3 =', x * 3);
console.log('5 / 2 =', x / 2);
// % - modulo czyli reszta z dzielenia
console.log('5 mod 2 =', x % 2);
console.log('5 mod 9 =', x % 9);
// ** - potęgowanie
console.log('5^2 =', x ** 2);
console.log('5^3 =', x ** 3);